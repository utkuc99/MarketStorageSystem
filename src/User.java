public class User {

    public int id_ ;
    public String firstName_;
    public String lastName_ ;
    public String loginName_;
    public String gender_ ;
    public String city_ ;

    public User(){ }

    public User(int id_, String firstName_, String lastName_, String loginName_, String gender_, String city_) {
        this.id_ = id_;
        this.firstName_ = firstName_;
        this.lastName_ = lastName_;
        this.loginName_ = loginName_;
        this.gender_ = gender_;
        this.city_ = city_;
    }


}
